local function HasValidWeapon(player)
    local classId = player:GetClass()
    local classItems = VALID_ITEMS[classId]
    if not classItems then
        return false
    end
    
    for _, item in ipairs(classItems) do
        local itemId = item[1]
        if player:HasItem(itemId) then
            return true
        end
    end
    return false
end

local function ReplaceWeapon(player)
    local classId = player:GetClass()
    local classItems = VALID_ITEMS[classId]
    local replacementItems = REPLACEMENT_ITEMS[classId]
    
    if not classItems or not replacementItems then
        return
    end
    
    for _, item in ipairs(classItems) do
        local itemId = item[1]
        local count = item[2] or 1
        local totalCount = player:GetItemCount(itemId)
        
        if totalCount > 0 then
            player:RemoveItem(itemId, totalCount)
        end
    end

    for _, item in ipairs(replacementItems) do
        local itemId = item[1]
        local count = item[2] or 1
        player:AddItem(itemId, count)
    end
end

local function OnGossipHello(event, player, creature)
    player:GossipClearMenu()
    player:GossipSetText('Hé hé, ' .. player:GetName() .. ', t’es dans une sale posture, hein ?\n\nTon arme... Tsss, elle t’est complètement inutile ! Heureusement pour toi, j’ai une offre *exclusive* : on l’échange et hop, tu repars avec une autre ! Marché conclu ?\n\n |cffffffff*Attention ! Assure-toi d’avoir assez de place dans ton sac avant de faire l’échange.*|r')  
	player:GossipMenuAddItem(2, "Tsss… Je le savais. Allez, fais l'échange.", 1, 1)  
	player:GossipMenuAddItem(7, "Pfff… Je sens que je vais le regretter, mais vas-y.", 1, 1) 
    player:GossipSendMenu(0x7FFFFFFF, creature)
end

local function OnGossipSelect(event, player, creature, sender, intid, code, menuid)
    if (intid == 1) then
        if player:HasQuest(QUEST_ID) then
            if HasValidWeapon(player) then
                ReplaceWeapon(player)
                player:CompleteQuest(QUEST_ID)
                player:SendBroadcastMessage("Vous avez reçu toutes les armes A6 ! Vous devez validée la quête à Chromie.")
            else
                player:SendBroadcastMessage("Vous devez posséder au moins une arme valide pour valider cette quête.")
            end
        else
            player:SendBroadcastMessage("Vous n'avez pas la quête nécessaire pour effectuer cet échange.")
        end
        player:GossipComplete()
    elseif (intid == 2) then
        player:SendNotification("Reviens me voir si tu veux échanger tes armes.")
        player:GossipComplete()
    end
end

