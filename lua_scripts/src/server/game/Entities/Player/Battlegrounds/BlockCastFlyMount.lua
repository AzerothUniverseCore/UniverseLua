-- Vérifie si le joueur est dans une zone interdite
local function IsRestrictedZone(player)
    local map = player:GetMap()
    return RESTRICTED_MAPS[map:GetMapId()] or map:IsDungeon() or map:IsRaid() or map:IsBattleground() or map:IsArena()
end

-- Empêche le lancement des sorts interdits
local function OnSpellCast(event, player, spell, skipCheck)
    if FORBIDDEN_SPELLS[spell:GetEntry()] and IsRestrictedZone(player) then
        player:SendBroadcastMessage("Les montures volantes ne peuvent pas être invoquées dans cette zone.")
        player:SendNotification("Les montures volantes ne peuvent pas être invoquées dans cette zone.")
        spell:Cancel() -- Annule immédiatement le cast en cours
        return false -- Bloque le lancement du sort
    end
end

-- Supprime les auras si elles sont appliquées
local function OnUpdateZone(event, player, newZone, newArea)
    for spellId, _ in pairs(FORBIDDEN_SPELLS) do
        if player:HasAura(spellId) and IsRestrictedZone(player) then
            player:RemoveAura(spellId) -- Supprime immédiatement l'aura
        end
    end
end

RegisterPlayerEvent(5, OnSpellCast)   -- Bloque le cast des sorts interdits
RegisterPlayerEvent(27, OnUpdateZone) -- Supprime les auras des sorts interdits si le joueur entre dans une zone restreinte
